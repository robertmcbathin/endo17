<h3><u>Проктология</u></h3><hr>
<p>Проктология – это область медицины, занимающаяся диагностикой и лечением заболеваний прямой кишки.</p>
<p><strong>ГЕМОРРОЙ</strong> – это заболевание, связанное с увеличением и выпадением сосудистых сплетений прямой кишки и заднего прохода. Среди факторов, способствующих развитию геморроя, наиболее значимыми являются  запоры, и наоборот  частые поносы, сидячая работа и малоподвижный образ жизни, тяжелый физический труд и силовые виды спорта, беременность и роды, злоупотребление алкоголем. Практически 70 % взрослого населения планеты за свою жизнь хотя бы раз сталкиваются с проявлениями геморроя.</p>
